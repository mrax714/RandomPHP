            <div class="clearfix text-formatted field field--name-field-slice-text field--type-text-long field--label-hidden field__item"><h2>What is WWF doing?</h2>

<p>For many years, WWF has worked with the Southern Ocean Commission (CCAMLR) to protect the most critical habitats for iconic species such as whales, penguins, seals, seabirds and their prey – tiny Antarctic krill. In the Southern Ocean, CCAMLR has made a commitment to implement a network of marine protected areas around Antarctica, to protect a range of wildlife impacted by climate change including areas where whales feed on tiny Antarctic krill. WWF works with scientists to provide important information to help governments protect these crucial foraging areas.</p>

<quote>“Antarctic blue whales are the largest animals on Earth and a spectacular wonder to witness in the wild. They are the true Antarctic giants. However, they are critically endangered, very slowly&nbsp; recovering from the impacts of 20th Century whaling. Now, we are working to protect the important foraging areas for blue whales in the Southern Ocean before it’s too late. This research reaffirms there is still hope.”, says Chris Johnson, Senior Manager, WWF Antarctic Programme.</quote>
