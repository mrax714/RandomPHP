	<h1>Remarkable Numbers of Antarctic Blue Whales Sighted in South Georgia</h1>
	<h3>Blue Whales</h3>
	
	<p><img class="align-left" src="/t/whatdoesawha.jpg" style="width:50%; height:auto;" />
	The Antarctic blue whale (Balaenoptera musculus ssp. Intermedia) is the largest animal on the planet, weighing up to 200 tons (approximately 33 elephants) and reaching up to 30 meters in length. The Antarctic blue whale has a heart the size of a small car, and during the main feeding season it consumes 3600 kg of krill per day. They are the loudest animals on Earth and are even louder than a jet engine. Their calls reach 188 decibels, while a jet reaches 140 decibels. Their low frequency whistle can be heard for hundreds of miles and is probably used to attract other blue whales.</p>

	<h3>WHY THE ANTARCTIC BLUE WHALE IS “CRITICALLY ENDANGERED”</h3>
<p>The Blue whale population in Antarctica was drastically reduced by commercial whaling, which started in South Georgia (South Atlantic) in 1904. Despite the legal protection through the International Whaling Commission in 1960’s, illegal hunting continued until 1972. From about 125,000 individuals in 1926, numbers were reduced to about 3,000 individuals in 2018, which classifies them as “critically endangered” on the IUCN Red List.
</p>


	<h3>SIGHTINGS DURING THE SOUTH GEORGIA EXPEDITION</h3>
<p>A team of scientists led by the British Antarctic Survey (BAS) were able to share some good news when returning from their recent expedition to the sub-Antarctic island of South Georgia. They counted 55 Antarctic blue whales during their 2020 expedition, which they describe as “unprecedented”. The South Georgia Waters remain an important summer feeding ground.
</p>
	<p>
		Dr Jennifer Jackson, a whale ecologist at BAS, says: “After three years of surveys,
		we are thrilled to see so many
		whales visiting South Georgia to feed again. This is a place where both whaling 
		and sealing were carried out extensively.
		It is clear that protection from whaling has worked, with humpback whales now seen at densities similar to those a
		century earlier, when whaling first began at South Georgia.”
	</p>
